from turtle import *
def cuatroP():
    pencolor("black")
    dot(10)
    goto(0,100)
    pencolor("green")
    dot(10)
    goto(0,100)
    pencolor("red")
    goto(100,0)
    dot(10)
    pencolor("blue")
    goto(100,100)
    dot(10)
    return()


#main
